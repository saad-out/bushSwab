#include "libft.h"

void	ft_lstadd_front(t_list **list, t_list *new);

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	if (!lst || !new)
		return ;
	tmp = *lst;
	if (!tmp)
		return (ft_lstadd_front(lst, new));
	while (tmp && tmp->next)
		tmp = tmp->next;
	tmp->next = new;
}
